/*
 __builtin_ctz 是一个内置函数，它可以返回一个整数的二进制表示中末尾的 0 的个数。
 例如，__builtin_ctz(64) 会返回 6，因为 64 的二进制是 1000000，末尾有 6 个 0。
 这个函数可以用来快速计算一个数的最低有效位（lowbit），也就是它的二进制表示中最右边的 1 所对应的值。
 例如，lowbit(12) = __builtin_ctz(12) = 4，因为 12 的二进制是 1100，最右边的 1 对应的值是 4。

类似的内置函数还有 __builtin_clz，它可以返回一个整数的二进制表示中前导的 0 的个数；
 __builtin_popcount，它可以返回一个整数的二进制表示中 1 的个数；
 __builtin_parity，它可以返回一个整数的二进制表示中 1 的个数的奇偶性。
 这些函数都有对应的 long long 类型的版本，只需要在函数名后面加上 ll 即可。
 例如，__builtin_ctzll 是 __builtin_ctz 的 long long 版本。
 */